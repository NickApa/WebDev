CREATE TABLE [Assignments] (
  [ID] Integer PRIMARY KEY IDENTITY(1, 1),
  [IMPORTANCE] Integer,
  [DUE] DATETIME,
  [TITLE] NVARCHAR(50),
  [NOTES] NVARCHAR(250),
  [COURSEID] int
)
GO

CREATE TABLE [Courses] (
  [ID] Integer PRIMARY KEY IDENTITY(1, 1),
  [NAME] NVARCHAR(10)
)
GO

CREATE TABLE [Tagnames] (
  [ID] Integer PRIMARY KEY IDENTITY(1, 1),
  [TAGNAME] NVARCHAR(20)
)
GO

CREATE TABLE [AssignmentTag] (
  [ID] Integer PRIMARY KEY IDENTITY(1, 1),
  [ASSIGNMENTID] int,
  [TAGNAMEID] int
)
GO

ALTER TABLE [Assignments] ADD CONSTRAINT ASSIGNMENTS_FK_COURSE FOREIGN KEY ([COURSEID]) REFERENCES [Courses] ([ID])
GO

ALTER TABLE [AssignmentTag] ADD CONSTRAINT TAG_FK_ASSIGNMENT FOREIGN KEY ([ASSIGNMENTID]) REFERENCES [Assignments] ([ID])
GO

ALTER TABLE [AssignmentTag] ADD CONSTRAINT TAG_FK_TAGNAME FOREIGN KEY ([TAGNAMEID]) REFERENCES [Tagnames] ([ID])
GO
